class PeopleList {
  String coverImage;
  String title;
  String summary;
  String content;
  String date;

  PeopleList(String coverImage, String title, String summary, String content, String date) {
    this.coverImage = coverImage;
    this.title = title;
    this.summary = summary;
    this.content = content;
    this.date = date;
  }
}

