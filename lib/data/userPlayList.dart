class UserPlayList {
  String avatar;
  String songTitle;
  String songCover;
  String songComment;
  String uploadDate;

  UserPlayList(String avatar, String songTitle, String songCover, String songComment, String uploadDate) {
    this.avatar = avatar;
    this.songTitle = songTitle;
    this.songCover = songCover;
    this.songComment = songComment;
    this.uploadDate = uploadDate;
  }
}