class HomeCarouselList {
  String avatar;
  String artistName;
  String content;
  String albumTitle;
  String albumCover;

  HomeCarouselList(String avatar, String artistName, String content, String albumTitle, String albumCover) {
    this.avatar = avatar;
    this.artistName = artistName;
    this.content = content;
    this.albumTitle = albumTitle;
    this.albumCover = albumCover;
  }
}